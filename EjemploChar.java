public class EjemploChar {

	public static void main (String [] args) {
		char letra1 = 'a';
		char letra2 = 'b';
	
		System.out.println(letra1);
		System.out.println(letra2);
		System.out.println(letra1 + letra2);
		System.out.println(letra1 + "" + letra2);

	}
}
